a = "Hello, World!"
print(a[2:5])           # Slicing: This prints characters in a set range within the string
print(a[:5])            # Slice from start: This prints characters from the beginning to a specific position
print(a[2:])            # Slice to the end: This prints characters from a specific position to the end
print(a[-6:-1])         # Negative Indexing: This prints characters from two, negative specified positions. Negative ndexing starts at 0 and starts on the right side